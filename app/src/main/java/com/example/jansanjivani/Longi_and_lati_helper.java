package com.example.jansanjivani;

public class Longi_and_lati_helper {

    String longi,lati ;


    public Longi_and_lati_helper( ) {
    }

    public Longi_and_lati_helper(String longi, String lati) {
        this.longi = longi;
        this.lati = lati;
    }

    public String getLongi() {
        return longi;
    }

    public void setLongi(String longi) {
        this.longi = longi;
    }

    public String getLati() {
        return lati;
    }

    public void setLati(String lati) {
        this.lati = lati;
    }
}
